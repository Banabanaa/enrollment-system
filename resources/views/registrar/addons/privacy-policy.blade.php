        <h2>4. How Staffs Use Student Information</h2>
        <p>Staff members are authorized to use student information within the System for the following purposes:</p>
        <ul>
            <li>Enrollment and Registration: To process applications for registration for courses, and program enrollment.</li>
            <li>Academic Administration: To process academic records, track progress, and issue COR.</li>
            <li>Communication: To communicate regarding enrollment, course information, announcements, and other relevant updates.</li>
            <li>Student Services: To provide student support, counseling, and other related services.</li>
            <li>Compliance with Laws and Regulations: To comply with legal obligations and regulatory requirements.</li>
        </ul>

        <h2>5. Legal Basis for Staff Processing</h2>
        <p>Staff processing of student information is based on the following:</p>
        <ul>
            <li><strong>Contractual Necessity:</strong> Processing is necessary to fulfill the Institution's contractual obligations to its students.</li>
            <li><strong>Legal Obligation:</strong> Processing is necessary for the Institution's compliance with relevant laws and regulations.</li>
            <li><strong>Legitimate Interest:</strong> Processing is necessary for the legitimate interests of the Institution in fulfilling its educational mission, provided these interests do not override the student's rights.</li>
            <li><strong>Consent:</strong> When handling sensitive personal information (such as health records or disability status), the staff member must ensure that the student's explicit consent has been obtained for each specific use case and purpose.</li>
        </ul>

        <h2>6. Data Sharing by Staff</h2>
        <p>Staff members are authorized to share student data only under the following circumstances:</p>
        <ul>
            <li>With Other Authorized Staff: Sharing is permitted with other staff members with a legitimate need to know for the purposes outlined in Section 4.</li>
            <li>Government Agencies: Sharing is permitted with government agencies as required by law.</li>
        </ul>
        <p>Staff members are prohibited from:</p>
        <ul>
            <li>Sharing student data with any unauthorized individuals.</li>
            <li>Using student data for purposes outside the scope of their responsibilities.</li>
            <li>Copying or transferring student data to personal devices or non-secure systems.</li>
            <li>Disclosing student information without proper authorization.</li>
        </ul>

        <h2>7. Staff Responsibilities for Data Security</h2>
        <p>Staff members are responsible for:</p>
        <ul>
            <li><strong>Maintaining Account Security:</strong> Keeping their System login credentials confidential and not sharing them with anyone.</li>
            <li><strong>Data Protection Practices:</strong> Following established institutional policies and procedures for accessing, processing, and handling student data.</li>
            <li><strong>Reporting Breaches:</strong> Promptly reporting any suspected data security breaches to the Institution’s Data Protection Officer (DPO).</li>
            <li><strong>Compliance Training:</strong> Completing all required data privacy and security training provided by the Institution.</li>
            <li><strong>Following Data Access Policies:</strong> Restricting access to student data to the minimum necessary for the job functions.</li>
            <li><strong>Adherence to the Data Minimization Principle:</strong> Collecting and maintaining only the minimum personal data necessary to support the Institution's specific and legitimate purposes.</li>
            <li>Maintaining data quality and accuracy</li>
        </ul>

